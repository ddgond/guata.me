// Freezes a quiz's data into a versioned snapshot for helloquiz.app, plus a
// setup doc for creating the quizzes there by hand.
//
// helloquiz map quizzes reference shapes by their index in an externally
// hosted GeoJSON file, so the file must never change once a quiz points at
// it. This script copies the live quiz data into
// public/data/helloquiz/<family>-v<N>.json — committed, deployed, and never
// regenerated in place (bumping the version is explicit, and old versions
// stay up forever) — and writes scripts/helloquiz/<family>-v<N>.md with one
// row per planned quiz: the property filter to type into helloquiz's shape
// selection, and the shape count to verify against.
//
// Each feature also gains a `band` property so the band-tier quizzes can be
// filtered with one exact value instead of a comma-list of regions.
//
// Usage: node scripts/helloquiz-snapshot.mjs <family> [--version N]

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname } from 'node:path';

// Region labels and band groupings duplicated from src/components/
// map-quiz-defs.ts (LANDKREIS_REGIONS / LANDKREIS_BANDS) — keep in sync when
// cutting a new version.
const FAMILIES = {
	landkreise: {
		source: 'public/data/landkreise.json',
		nameProp: 'name',
		regionProp: 'region',
		baseLabel: 'All Germany',
		regions: {
			north: 'Schleswig-Holstein, Hamburg & MV',
			niedersachsen: 'Niedersachsen & Bremen',
			brandenburg: 'Berlin & Brandenburg',
			'sachsen-anhalt': 'Sachsen-Anhalt',
			sachsen: 'Sachsen',
			thueringen: 'Thüringen',
			westfalen: 'Westfalen-Lippe',
			rheinland: 'Rheinland',
			hessen: 'Hessen',
			'rheinland-pfalz': 'Rheinland-Pfalz',
			saarland: 'Saarland',
			bw: 'Baden-Württemberg',
			nordbayern: 'Nordbayern (Franken & Oberpfalz)',
			suedbayern: 'Südbayern',
		},
		bands: {
			north: {
				label: 'North Germany',
				regions: ['north', 'niedersachsen', 'brandenburg', 'sachsen-anhalt', 'sachsen'],
			},
			central: {
				label: 'Central Germany',
				regions: ['thueringen', 'westfalen', 'rheinland', 'hessen', 'rheinland-pfalz', 'saarland'],
			},
			south: {
				label: 'South Germany',
				regions: ['bw', 'nordbayern', 'suedbayern'],
			},
		},
	},
};

const [familyKey, ...rest] = process.argv.slice(2);
const family = FAMILIES[familyKey];
if (!family) {
	console.error(`Usage: node scripts/helloquiz-snapshot.mjs <family> [--version N]`);
	console.error(`Families: ${Object.keys(FAMILIES).join(', ')}`);
	process.exit(1);
}
const versionFlag = rest.indexOf('--version');
const version = versionFlag === -1 ? 1 : Number(rest[versionFlag + 1]);
if (!Number.isInteger(version) || version < 1) {
	console.error(`--version takes a positive integer, got: ${rest[versionFlag + 1]}`);
	process.exit(1);
}

const snapshotPath = `public/data/helloquiz/${familyKey}-v${version}.json`;
const docPath = `scripts/helloquiz/${familyKey}-v${version}.md`;
const snapshotUrl = `https://guata.me/data/helloquiz/${familyKey}-v${version}.json`;
if (existsSync(snapshotPath)) {
	console.error(`${snapshotPath} already exists. Snapshots are frozen once written —`);
	console.error(`helloquiz quizzes reference shapes by index, so regenerating one in`);
	console.error(`place breaks every quiz pointing at it. To publish updated data, cut`);
	console.error(`a new version (--version ${version + 1}) and repoint the quizzes.`);
	process.exit(1);
}

const bandOf = {};
for (const [bandKey, band] of Object.entries(family.bands)) {
	for (const region of band.regions) {
		if (!(region in family.regions)) {
			throw new Error(`Band ${bandKey} lists unknown region ${region}`);
		}
		if (bandOf[region]) throw new Error(`Region ${region} is in two bands`);
		bandOf[region] = bandKey;
	}
}

const { features } = JSON.parse(readFileSync(family.source, 'utf8'));
const counts = {};
const snapshotFeatures = features.map((feature) => {
	const region = feature.properties[family.regionProp];
	if (!bandOf[region]) {
		throw new Error(`Feature ${feature.properties[family.nameProp]} has region ${region}, which is in no band`);
	}
	counts[region] = (counts[region] ?? 0) + 1;
	return { ...feature, properties: { ...feature.properties, band: bandOf[region] } };
});

// One feature per line, like the mapshaper output in public/data, so the
// committed file diffs sanely
mkdirSync(dirname(snapshotPath), { recursive: true });
writeFileSync(
	snapshotPath,
	`{"type":"FeatureCollection", "features": [\n${snapshotFeatures
		.map((f) => JSON.stringify(f))
		.join(',\n')}\n]}\n`,
);

const bandCount = (band) => band.regions.reduce((sum, region) => sum + counts[region], 0);
const row = (coverage, filter, shapes) => `| ${coverage} | ${filter} | ${shapes} |`;
const doc = `# helloquiz setup — ${familyKey} v${version}

Generated by \`scripts/helloquiz-snapshot.mjs\`. One row per quiz to create on
[helloquiz.app](https://helloquiz.app); quiz titles and descriptions are yours
to write on the site — the Coverage column is just the label the same drill
has on guata.me.

**GeoJSON URL** (must be deployed before helloquiz can fetch it):

    ${snapshotUrl}

This snapshot is frozen: helloquiz references shapes by index, so the file
must never change. Updated data means a new version and repointing.

## Steps

1. Create the base quiz as a map quiz with the GeoJSON URL above, no shape
   filter, and questions auto-imported from the \`${family.nameProp}\` property.
2. Create each band and region quiz as a **remix of the base quiz**, changing
   only the shape filter to the single property=value below.
3. After each import, check the shape count against the table.

## Quizzes

| Coverage | Shape filter | Shapes |
| --- | --- | --- |
${[
	row(`${family.baseLabel} (base quiz)`, 'none', features.length),
	...Object.entries(family.bands).map(([key, band]) =>
		row(band.label, `\`band\` = \`${key}\``, bandCount(band)),
	),
	...Object.entries(family.regions).map(([key, label]) =>
		row(label, `\`${family.regionProp}\` = \`${key}\``, counts[key]),
	),
].join('\n')}
`;
mkdirSync(dirname(docPath), { recursive: true });
writeFileSync(docPath, doc);

console.log(`Wrote ${snapshotPath} (${features.length} features)`);
console.log(`Wrote ${docPath}`);
